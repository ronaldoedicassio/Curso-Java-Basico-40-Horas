        float numerador = Float.parseFloat(txtNumerador.getText()); 
        float denomidador = Float.parseFloat(txtDenominador.getText());
        
        float div = numerador / denomidador;
        float res = numerador % denomidador;
        txtDivisao.setText(Float.toString(div).format("%.2f",div));
        txtResto.setText(Float.toString(res));
        
        
        
